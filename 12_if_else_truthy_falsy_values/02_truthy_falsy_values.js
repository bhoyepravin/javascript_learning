// truthy and falsy values

// falsy values
// false
// "" empty String
// null
// undefined
// 0

let firstName = "";

if (firstName) {
  console.log("Name is present");
} else {
  console.log("Name is not present");
}

// truthy values
// "abc"
// 1, -1, 2, 3
// "0" (string with zero)

let lastName = "Pravin";

if (lastName) {
  console.log("Name is present");
} else {
  console.log("Name is not present");
}
