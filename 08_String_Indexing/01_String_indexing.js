// string indexing
// string indexing is used to access individual characters in a string

let firstName = "Pravinbhoye";

// accessing characters using indexing
// p     r      a     v     i     n
// 0     1      2     3     4     5

console.log(firstName[4]);

// length of string
// firstName.length gives the length of the string
// space is also counted as a character
console.log(firstName.length); // Output: 6

// last index : length -1

console.log(firstName[firstName.length - 2]);
